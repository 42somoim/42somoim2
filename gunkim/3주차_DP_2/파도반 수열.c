#include <stdio.h>

int		main(void)
{
	long	i, n, q;
	long	mem[101];

	mem[1] = 1;
	mem[2] = 1;
	mem[3] = 1;
	mem[4] = 2;
	mem[5] = 2;
	mem[6] = 3;
	i = 7;
	while (i <= 100)
	{
		mem[i] = mem[i - 1] + mem[i - 5];
		i++;
	}
	scanf("%ld", &n);
	i = 0;
	while (i < n)
	{
		scanf("%ld", &q);
		printf("%ld\n", mem[q]);
		i++;
	}
	return (0);
}
