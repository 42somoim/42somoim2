#include <stdio.h>

int		main(void)
{
	int		mem[201][201] = {};
	int		n, k;
	int		x, y;
	

	scanf("%d", &n);
	scanf(" %d", &k);
	x = 0;
	while (++x <= k)
		mem[0][x] = 1;
	y = 0;
	while (++y <= n)
		mem[y][1] = 1;
	y = 1;
	while (y <= n)
	{
		x = 2;
		while (x <= k)
		{
			mem[y][x] = (mem[y - 1][x] + mem[y][x - 1]) % 1000000000;
			x++;
		}
		y++;
	}
	printf("%d", mem[n][k]);
}
