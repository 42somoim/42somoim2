#include <stdio.h>

unsigned int	mem[1024];

int		main(void)
{
	int		x;
	int		i;

	scanf("%d", &x);
	mem[1] = 1;
	mem[2] = 2;
	i = 2;
	while (++i <= x)
		mem[i] = (mem[i - 1] + mem[i - 2]) % 10007;
	printf("%u\n", mem[x]);
	return (0);
}
