#include <stdio.h>

int		ft_error(void)
{
	printf("0");
	return (0);
}

int		main(void)
{
	char	s[5001] = {0};
	int		mem[5001][3] = {0};
	int		i;

	scanf("%s", s);
	if (s[0] == '0')
		return (ft_error());
	mem[0][0] = 1;
	mem[0][1] = 1;
	i = 1;
	while (s[i])
	{
		if ((s[i - 1] != '1' && s[i - 1] != '2') && s[i] == '0')
			return (ft_error());
		if ((s[i - 1] == '1' && '0' == s[i]) ||
					(s[i - 1] == '2' && '0' == s[i]))
		{
			mem[i][1] = mem[i - 2][0];
			mem[i][2] = 0;
			mem[i][0] = mem[i - 1][1] % 1000000;
		}
		else if ((s[i - 1] == '1' && ('0' <= s[i] && s[i] <= '9')) ||
					(s[i - 1] == '2' && ('0'<= s[i] && s[i] <= '6')))
		{
			mem[i][1] = mem[i - 1][0];
			mem[i][2] = mem[i - 1][1];
			mem[i][0] = (mem[i][1] + mem[i][2]) % 1000000;
		}
		else
		{
			mem[i][1] = mem[i - 1][0];
			mem[i][2] = 0;
			mem[i][0] = mem[i][1] % 1000000;
		}
		i++;
	}

	printf("%d", mem[i - 1][0] % 1000000);
	return (0);
}
