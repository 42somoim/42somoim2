#include <stdio.h>

int		main(void)
{
	int		mon[12] = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
	char	day[7][4] = {"MON", "TUE", "WED", "THU", "FRI", "SAT", "SUN"};
	int		x, y;
	int		days;

//	(1, 1) -> (0, 0) -> MON;
//	(1, 29) -> (0, 28) -> 28 % 7 = 0 -> MON; 
//	(1, 31) -> (0, 30) -> 30 % 7 = 2 -> TUE;
//	(2, 1) -> (1, 0) -> (31) % 7 = 3 -> WED;

	scanf("%d %d", &x, &y);
	days = 0;
	while (x - 1)
	{
		days += mon[x - 2];
		x--;
	}
	days += y - 1;
	printf("%s\n", day[days % 7]);
	return (0);
}
