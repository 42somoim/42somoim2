#include <stdio.h>

int		main(void)
{
	size_t	x, i;
	size_t	mem[91][3] = {};

	scanf("%lu", &x);
	mem[1][0] = 0;
	mem[1][1] = 1;
	i = 1;
	while (++i <= x)
	{
		mem[i][0] = mem[i - 1][0] + mem[i - 1][1];
		mem[i][1] = mem[i - 1][0]; 
	}
	printf("%lu", mem[x][0] + mem[x][1]);
	return (0);
}